
package rectangle;

/**
 * Class for all geometric figures.
 * 
 * @author Alex Vasil
 */
public abstract class GeometricFigures {
   protected double height;
   protected double width;
   
   /**
    * Computes the area of the geometric figure.
    * 
    * @return Area of the figure.
    */
   public abstract double computeArea();
}
